--[[
    Warband Nexus - Turkish (trTR)

    WoW has no Turkish game client, so AceLocale would never auto-select this locale.
    Like every other locale it loads into ns.LOCALES (see enUS.lua) and Core.lua selects
    it when db.profile.languageOverride == "trTR". Keys omitted here fall back to enUS /
    GlobalStrings, so Blizzard game vocabulary and proper nouns intentionally stay untranslated.

    Curate only clearly translatable addon copy here. Do NOT add:
      - PARSE_* keys (matched against English Blizzard tooltip text)
      - Game proper nouns (Great Vault, Mythic+, Torghast, Warband, keystone, ...)

    GlobalString-backed keys DO need overrides here, unlike in every other locale.
    enUS defines e.g. L["SETTINGS_PANEL_GENERAL"] = GENERAL or "General". For deDE/frFR
    the client supplies a native GENERAL, so omitting the key is correct. There is no
    Turkish client, so GENERAL / DESCRIPTION / SOURCE / MOUNTS / BATTLE_PET_SOURCE_*
    resolve to the player's client language and leak English into an otherwise Turkish
    UI (this is why "Konum:" was Turkish but "Description:" was not). Override the
    generic vocabulary; leave PARSE_* and proper nouns alone.

    File must stay UTF-8 without BOM. Native Turkish letters required (ç ğ ı İ ö ş ü).
]]

